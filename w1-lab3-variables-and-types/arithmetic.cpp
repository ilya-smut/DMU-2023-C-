#include <iostream>

using namespace std;

int main()
{
    int length;
    int width;
    length = 10;
    width = 20;
    cout << "length = " << length << endl;
    cout << "width = " << width << endl;
    cout << "length + width = " << length + width << endl;
    cout << "length - width = " << length - width << endl;
    cout << "length * width = " << length * width << endl;
    cout << "length / width = " << length / width << endl;
}