#include <iostream>

using namespace std;

int main()
{
    cout << "The size of type int is " << sizeof(int) << " bytes\n";
    cout << "The size of type short is " << sizeof(short) << " bytes\n";
    cout << "The size of type float is " << sizeof(float) << " bytes\n";
    cout << "The size of type double is " << sizeof(double) << " bytes\n";
    cout << "The size of type char is " << sizeof(char) << " bytes\n";
    cout << "The size of type bool is " << sizeof(bool) << " bytes\n";
    cout << "The size of type string is " << sizeof(string) << " bytes\n";
}